/**
 * Provides the base implementation for the Insurance Plan UAD anonymizer.
 *
 * <p>
 * This implementation exists only as a container for the default methods
 * generated by ServiceBuilder. All custom service methods should be put in
 * {@link InsurancePlanUADAnonymizer}.
 * </p>
 *
 * @author Brian Wing Shun Chan
 * @generated
 */
public abstract class BaseInsurancePlanUADAnonymizer
	extends DynamicQueryUADAnonymizer<InsurancePlan> {

	@Override
	public void autoAnonymize(
			InsurancePlan insurancePlan, long userId, User anonymousUser)
		throws PortalException {

		if (insurancePlan.getUserId() == userId) {
			insurancePlan.setUserId(anonymousUser.getUserId());
			insurancePlan.setUserName(anonymousUser.getFullName());

			autoAnonymizeAssetEntry(insurancePlan, anonymousUser);
		}

		if (insurancePlan.getStatusByUserId() == userId) {
			insurancePlan.setStatusByUserId(anonymousUser.getUserId());
			insurancePlan.setStatusByUserName(anonymousUser.getFullName());
		}

		insurancePlanLocalService.updateInsurancePlan(insurancePlan);
	}

	@Override
	public void delete(InsurancePlan insurancePlan) throws PortalException {
		insurancePlanLocalService.deleteInsurancePlan(insurancePlan);
	}

	@Override
	public Class<InsurancePlan> getTypeClass() {
		return InsurancePlan.class;
	}

	protected void autoAnonymizeAssetEntry(
		InsurancePlan insurancePlan, User anonymousUser) {

		AssetEntry assetEntry = fetchAssetEntry(insurancePlan);

		if (assetEntry != null) {
			assetEntry.setUserId(anonymousUser.getUserId());
			assetEntry.setUserName(anonymousUser.getFullName());

			assetEntryLocalService.updateAssetEntry(assetEntry);
		}
	}

	@Override
	protected ActionableDynamicQuery doGetActionableDynamicQuery() {
		return insurancePlanLocalService.getActionableDynamicQuery();
	}

	@Override
	protected String[] doGetUserIdFieldNames() {
		return CIBTUADConstants.USER_ID_FIELD_NAMES_INSURANCE_PLAN;
	}

	protected AssetEntry fetchAssetEntry(InsurancePlan insurancePlan) {
		return assetEntryLocalService.fetchEntry(
			InsurancePlan.class.getName(), insurancePlan.getInsurancePlanId());
	}

	@Reference
	protected AssetEntryLocalService assetEntryLocalService;

	@Reference
	protected InsurancePlanLocalService insurancePlanLocalService;

}